    public ArrayList<PostModel> postList(){
        ArrayList<PostModel> postList = new ArrayList<>();
    
       try{
           String driver = "com.mysql.cj.jdbc.Driver";  
           Class.forName(driver);
           String Url= "jdbc:mysql://localhost/bloggy";
           String User = "root";
           String Password = "";
           Connection conn = DriverManager.getConnection(Url,User,Password);
           String queryl= "SELECT *FROM post";
           Statement state = conn.createStatement();
           ResultSet resultset= state.executeQuery(queryl);
           PostModel post;
           while(resultset.next()){
               post=new PostModel(resultset.getInt("id"),resultset.getString("title"),resultset.getString("description"),resultset.getString("content"));
               postList.add(post);
           }
       }catch(Exception e){
           JOptionPane.showConfirmDialog(null, e);
       }
       return postList;
    }
    public void show_post(){
        ArrayList<PostModel> list = postList();
        
        DefaultTableModel model =(DefaultTableModel)showall.getModel();
        Object[] row = new Object [4];
        for(int i= 0; i<list.size();i++){
            row[0]=list.get(i).getId();
            row[1]=list.get(i).getTitle();
            row[2]=list.get(i).getDescription();
            row[3]=list.get(i).getContent();
            model.addRow(row);
        }
    }

