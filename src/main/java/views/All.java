    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String id = jTextField1.getText();
        PostController postcontroller = new PostController();

        PostModel post = postcontroller.searchId(Integer.parseInt(id));

        if(post == null){

            JOptionPane.showMessageDialog(null,"Article n'existe pas");
        }else{
            jTextField2.setText(post.getTitle());
            jTextArea1.setText(post.getDescription());
            jTextArea2.setText(post.getContent());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int id = Integer.parseInt(jTextField1.getText());
        
        
        PostController postcontroller= new PostController();
        
        PostModel post  = postcontroller.searchId(id);
        
         if(post==null){
            JOptionPane.showMessageDialog(null,"Post not found");
        }else{
        
            postcontroller.delete(id);
        
            jTextField1.setText("");
            jTextField2.setText("");
            jTextArea1.setText("");
            jTextArea2.setText("");
         }
    }//GEN-LAST:event_jButton3ActionPerformed

